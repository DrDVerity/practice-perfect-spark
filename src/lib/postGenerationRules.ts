export const UNIVERSAL_CREATIVE_RULES = `
# 2. Universal Creative Rules (Applies to All Platforms)

## 2.1 Hook

First line / first 3 seconds must:
- Name a specific problem or symptom ("Bleeding when you floss?")
- Promise a specific outcome ("A same‑day crown in one visit")
- Or directly call out the local audience ("If you live in [city] and hate long dental visits…")

Avoid generic hooks ("Healthy habits matter") unless immediately localized and made concrete.

## 2.2 Value and Proof

Include at least one of:
- A "when to come in" rule of thumb ("If this has lasted more than 3 days, call us")
- A mini before/after scenario (HIPAA‑safe and de‑identified)
- A specific, believable metric ("Most patients are in and out in under 60 minutes")
- A short social proof point (review snippet, years in practice, number of patients served)

Use plain language and avoid jargon; if you must name a procedure, pair it with a lay explanation.

## 2.3 Call to Action and Friction Reduction

Give one primary action:
- "Tap to see today's openings."
- "Click to check prices and options."
- "DM 'SLEEP' for a free screening checklist."

Reduce friction explicitly for this demographic:
- Mention online booking, text confirmations, evening/Saturday hours, or membership for uninsured patients
- Clarify cost orientation if relevant ("Insurance welcome," "Membership starts around $X/mo.")

## 2.4 Visuals

Favor real clinicians, staff, and space over stock.

Show:
- Smiling staff at the front desk or chairside
- The environment (clean, modern, calm)
- Simple diagrams or before/after illustrations that don't reveal identifiable patient info

Keep visuals simple, bright, and legible on a phone; minimal on‑image text (3–7 words) highlighting the main benefit ("Same‑day crowns," "Gentle family dentistry").

## 2.5 Compliance and Trust

- Never reference specific patient identities or conditions without explicit, documented consent and appropriate de‑identification
- Don't diagnose or give individual medical advice in comments or DMs; route to phone, portal, or visit
- Avoid fear‑mongering; frame posts as reassurance and guidance, not threats
`;

